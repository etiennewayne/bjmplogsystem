<p><strong>Overview</strong></p>
<p style="text-align: justify;">As one of the five pillars of the Criminal Justice System, the BJMP was created to address growing concern of jail management and penology problem. Primarily, its clients are detainees accused before a court who are temporarily confined in such jails while undergoing investigation, waiting final judgement and those who are serving sentence promulgated by the court 3 years and below. As provided for under R.A. No. 6975, the Jail Bureau is mandated to take operational and administrative control over all city, district and municipal jails. The Bureau has four major areas of rehabilitation program, namely: Livelihood Projects, Educational and Vocational Training, Recreation and Sports, and Religious/ Spiritual Activities. These were continuously implemented to eliminate the offenders' pattern of criminal behaviour and to reform them to become law-abiding and productive citizens. Although the workplace of the Jail Bureau is confined inside the portals of jail to safeguard PDL, nonetheless, the Bureau has an inherent function of informing the public of jail operations and other matters concerning the corrections pillar of the Philippines. Coincidentally, being a new and growing Bureau, BJMP aims to keep the public abreast of information regarding jail management and penology.</p>
